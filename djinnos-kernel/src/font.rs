/// Draw a single character at pixel (x, y) with given scale and colour.
/// `scale` = 1 means 8×8 px; `scale` = 4 means 32×32 px on screen.
#[inline(never)]
pub fn draw_char(
    gpu:   &dyn crate::gpu::GpuSurface,
    x:     u32, y: u32,
    ch:    u8,
    scale: u32,
    r: u8, g: u8, b: u8,
) {
    let idx = ch.saturating_sub(32) as usize;
    if idx >= FONT.len() / 8 { return; }
    let rows = &FONT[idx * 8 .. idx * 8 + 8];
    for (row, &byte) in rows.iter().enumerate() {
        for col in 0..8u32 {
            if byte & (0x80 >> col) != 0 {
                let px = x + col * scale;
                let py = y + row as u32 * scale;
                for dy in 0..scale {
                    for dx in 0..scale {
                        gpu.set_pixel(px + dx, py + dy, b, g, r);
                    }
                }
            }
        }
    }
}

/// Draw a string; returns the x position after the last character.
#[inline(never)]
pub fn draw_str(
    gpu:   &dyn crate::gpu::GpuSurface,
    x:     u32, y: u32,
    s:     &str,
    scale: u32,
    r: u8, g: u8, b: u8,
) -> u32 {
    let mut cx = x;
    for ch in s.bytes() {
        draw_char(gpu, cx, y, ch, scale, r, g, b);
        cx += GLYPH_W * scale;
    }
    cx
}